public class Typecasting
{
public static void main(String[] args)
{
int i=51;short s=(short)i;long l=(long)i;double d=(double)i;float f=(float)i;char c=(char)i;
System.out.println("typecasting of short:"+s);
System.out.println("typecasting of long:"+l);
System.out.println("typecasting of double:"+d);
System.out.println("typecasting of float:"+f);
System.out.println("typecasting of char:"+c);
}
}
